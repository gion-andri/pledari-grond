package de.uni_koeln.spinfo.maalr.conjugator.verbs;

public class Reflex {

	
	private String verb;
	private String prefix;
	
	
	public String getVerb() {
		return verb;
	}
	public void setVerb(String verb) {
		this.verb = verb;
	}
	public String getPrefix() {
		return prefix;
	}
	public void setPrefix(String prefix) {
		this.prefix = prefix;
	}
	
	
	
}
