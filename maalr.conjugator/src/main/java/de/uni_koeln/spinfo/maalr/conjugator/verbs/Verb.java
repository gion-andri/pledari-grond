package de.uni_koeln.spinfo.maalr.conjugator.verbs;

public class Verb {

	private String form;
	private boolean stress;

	public String getForm() {
		return form;
	}

	public void setForm(String form) {
		this.form = form;
	}

	public boolean isStress() {
		return stress;
	}

	public void setStress(boolean stress) {
		this.stress = stress;
	}

}
